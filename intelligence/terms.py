def get_daily_terms(n: int = 2) -> list[tuple[str, str, str, str]]:
    """오늘 날짜 기반으로 n개 용어 선택 (매일 다른 조합)."""
    day_index = datetime.now().timetuple().tm_yday  # 1-365
    total = len(TERMS)
    indices = [(day_index * 2 + i) % total for i in range(n)]
    return [TERMS[i] for i in indices]
